    public String getCardNowWeatherCode() {
        return cardNowWeatherCode;
    }

    public void setCardNowWeatherCode(String cardNowWeatherCode) {
        this.cardNowWeatherCode = cardNowWeatherCode;
    }

    public String getCardNowTemp() {
        return cardNowTemp;
    }

    public void setCardNowTemp(String cardNowTemp) {
        this.cardNowTemp = cardNowTemp;
    }
